package plants.growthData;

public class PlantingDensityMinimum {

    private float sqm;
    private String acre;

    public PlantingDensityMinimum() {
    }

    public float getSqm() {
        return sqm;
    }

    public void setSqm(float sqm) {
        this.sqm = sqm;
    }

    public String getAcre() {
        return acre;
    }

    public void setAcre(String acre) {
        this.acre = acre;
    }
}
