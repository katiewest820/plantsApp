package plants.growthData;

public class RootDepthMinimum {

    private double inches;
    private double cm;

    public RootDepthMinimum() {
    }

    public double getInches() {
        return inches;
    }

    public void setInches(double inches) {
        this.inches = inches;
    }

    public double getCm() {
        return cm;
    }

    public void setCm(double cm) {
        this.cm = cm;
    }
}
