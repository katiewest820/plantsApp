package plants.growthData;

public class PrecipitationMinimum {

    private float inches;
    private float cm;

    public PrecipitationMinimum() {
    }

    public float getInches() {
        return inches;
    }

    public void setInches(float inches) {
        this.inches = inches;
    }

    public float getCm() {
        return cm;
    }

    public void setCm(float cm) {
        this.cm = cm;
    }
}
